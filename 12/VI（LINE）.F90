	SUBROUTINE VI(N,DX,P,V)
	DIMENSION P(N),V(N)
	COMMON /COMAK/AK(0:1100)
	PAI1=0.318309886
	C=ALOG(DX)
	DO 10 I=1,N
	V(I)=0.0
	DO 10 J=1,N
	IJ=IABS(I-J)
10	V(I)=V(I)+(AK(IJ)+C)*DX*P(J)
	DO I=1,N
	V(I)=-PAI1*V(I)
	ENDDO
	RETURN
	END
	SUBROUTINE SUBAK(MM)
	COMMON /COMAK/AK(0:1100)
	DO 10 I=0,MM
10  AK(I)=(I+0.5)*(ALOG(ABS(I+0.5))-1.)-(I-0.5)*(ALOG(ABS(I-0.5))-1.)
	RETURN
	END
